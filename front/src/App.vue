<script>
  import axios from 'axios'
  export default {
      name: 'App',
      data() {
          return {
              category: {
                  top: '001',
                  outer: '002',
                  pants: '003',
                  bag: '004',
                  sneakers: '018',
                  shoes: '005',
                  headwear: '007',
                  skirt: '022',
                  onepiece: '020',
                  socks: '008'
              },
              numToCategory: {
                  '001': 'Top',
                  '002': 'Outer',
                  '003': 'Pants',
                  '004': 'Bag',
                  '018': 'Sneakers',
                  '005': 'Shoes',
                  '007': 'Headwear',
                  '022': 'Skirt',
                  '020': 'Onepiece',
                  '008': 'Socks/Legwear'
              },
              currentListTopic : "",
              products: [],
              currentPage: 1,
              curCategory: '',
              curBrand: '',
              rows: 0,
              perpage: 25,
              brands : [],
          }
      },
      methods: {
          goToCategory(category, page) {
              let self = this
              self.currentListTopic = "category"
              window.scrollTo(0, 0);
              axios
                  .get('http://118.67.143.24:30000/api/v1/product/list', {
                      params: {
                          "category": category,
                          "page" : page
                      }
                  })
                  .then((response) => {
                      self.products = response.data.content
                      self.currentPage = response.data.pageable.pageNumber + 1
                      self.rows = response.data.totalElements
                      self.perpage = response.data.pageable.pageSize
                      self.curCategory = category
                      if (this.$route.path !== '/') 
                          this
                              .$router
                              .push({name: 'List'})
                      })
                  .catch((error) => {
                      console.log(error);
                  });
          },
         
          findBrandList(typeNumber) {
            alert(3)
              let self = this
              axios
              .get('http://118.67.143.24:30000/api/v1/search/brands', {
                  params: {
                      "type": typeNumber
                  }
              })
              .then((response) => {
                  self.brands = response.data
              })
              .catch((error) => {
                  console.log(error);
              });
          }
      },
      created() {
          let self = this
          self.currentListTopic = "category"
          self.curCategory = '001'
          self.goToCategory(self.curCategory, 0) 
          self.findBrandList(1) 
      }
  }
</script>

